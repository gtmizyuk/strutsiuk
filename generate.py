def audiozbirka_pager(current_slug):
    slugs = [s for s, _ in AUDIOZBIRKA_ORDER]
    if current_slug not in slugs:
        return ""
    idx = slugs.index(current_slug)
    if idx > 0:
        prev_slug, prev_title = AUDIOZBIRKA_ORDER[idx - 1]
        prev_html = '<a class="pager-link pager-prev" href="{href}"><i class="fa-solid fa-arrow-left"></i> {title}</a>'.format(
            href=href_from(current_slug, prev_slug), title=prev_title)
    else:
        prev_html = '<span class="pager-link pager-disabled"><i class="fa-solid fa-arrow-left"></i> Попередня</span>'
    if idx < len(AUDIOZBIRKA_ORDER) - 1:
        next_slug, next_title = AUDIOZBIRKA_ORDER[idx + 1]
        next_html = '<a class="pager-link pager-next" href="{href}">{title} <i class="fa-solid fa-arrow-right"></i></a>'.format(
            href=href_from(current_slug, next_slug), title=next_title)
    else:
        next_html = '<span class="pager-link pager-disabled">Наступна <i class="fa-solid fa-arrow-right"></i></span>'
    return '<nav class="pager" aria-label="Навігація між аудіозбірками">{}{}</nav>'.format(prev_html, next_html)


