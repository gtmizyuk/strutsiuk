def work_item(title, year=None, note=None, cover=True, cover_file=None):
    year_html = '<span class="work-year">{}</span>'.format(year) if year else ""
    note_html = '<span class="work-note">{}</span>'.format(note) if note else ""
    cover_html = '<div class="work-cover">{}</div>'.format(cover_img(title, cover_file)) if cover else ""
    return ('<li>{cover}<div class="work-text"><span class="work-title">{title}</span>{year}{note}</div></li>'
            ).format(cover=cover_html, title=title, year=year_html, note=note_html)


def gallery_cluster_card(filenames, caption):
    thumbs = "".join(
        '<img src="../img/gallery/{}" alt="{}" loading="lazy">'.format(f, caption)
        for f in filenames
    )
    return ('<div class="gallery-card gallery-cluster">'
            '<div class="gallery-cluster-grid">{thumbs}</div>'
            '<div class="gallery-caption">{caption}</div></div>'
            ).format(thumbs=thumbs, caption=caption)


def gallery_card(filename, caption):
    return ('<div class="gallery-card">{img}<div class="gallery-caption">{caption}</div></div>'
            ).format(img=gallery_img(filename, caption), caption=caption)


def quote_card(quote, name, note=None, photo=None):
    cite = name if not note else "{}, {}".format(name, note)
    photo_html = ""
    if photo:
        photo_html = '<img class="quote-photo" src="../img/critics/{}" alt="{}" loading="lazy">'.format(photo, name)
    return ('<div class="quote-card">{photo}<div class="quote-body"><p>{quote}</p><cite>{cite}</cite></div></div>'
            ).format(photo=photo_html, quote=quote, cite=cite)


