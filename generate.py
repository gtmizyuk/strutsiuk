SECTION_DIR = {}   # slug -> "pages" | "pages/<top-slug>"
PARENT_CHAIN = {}  # slug -> [(slug, title), ...] предки без себе


def index_tree(nodes, section_dir, chain):
    for slug, title, children, content_key, icon in nodes:
        if section_dir is None:
            this_dir = "pages/{}".format(slug) if children else "pages"
        else:
            this_dir = section_dir
        SECTION_DIR[slug] = this_dir
        PARENT_CHAIN[slug] = chain
        if children:
            index_tree(children, this_dir, chain + [(slug, title)])


index_tree(TREE, None, [])


def filename_for(slug):
    if slug == "":
        return "index.html"
    sd = SECTION_DIR[slug]
    if sd == "pages":
        return "pages/{}.html".format(slug)
    top = sd.split("/", 1)[1]
    if slug == top:
        return "{}/index.html".format(sd)
    return "{}/{}.html".format(sd, slug)


def href_from(current_slug, target_slug):
    cur_dir = posixpath.dirname(filename_for(current_slug)) or "."
    tgt_file = filename_for(target_slug)
    rel = posixpath.relpath(tgt_file, cur_dir)
    if not rel.startswith("."):
        rel = "./" + rel
    return rel


def asset_prefix(current_slug):
    cur_dir = posixpath.dirname(filename_for(current_slug))
    if not cur_dir:
        return "./"
    return posixpath.relpath(".", cur_dir) + "/"


