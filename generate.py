def track_card(title, url=None, words=None, music=None, performers=None, recorded=None, lyrics_text=None):
    if url:
        player_html = '<audio class="track-audio" controls preload="none" src="{}"></audio>'.format(url)
    else:
        player_html = """
        <div class="player-bar">
          <span class="player-play">{play_icon}</span>
          <span class="player-track"></span>
          <span class="player-time">00:00 / --:--</span>
        </div>
        <p class="player-note">Аудіозапис буде додано.</p>
        """.format(play_icon='<i class="fa-solid fa-play"></i>')

    meta_lines = []
    if words:
        meta_lines.append('<strong>Слова:</strong> {}'.format(words))
    if music:
        meta_lines.append('<strong>Музика:</strong> {}'.format(music))
    if performers:
        meta_lines.append('<strong>Виконує:</strong> {}'.format(performers))
    if recorded:
        meta_lines.append('<strong>Запис:</strong> {}'.format(recorded))
    meta_html = "".join('<p class="track-meta">{}</p>'.format(m) for m in meta_lines)

    if lyrics_text:
        lyrics_html = '<details class="lyrics-toggle"><summary>Текст пісні</summary><p>{}</p></details>'.format(lyrics_text)
    else:
        lyrics_html = '<p class="player-note">Текст пісні поки не додано.</p>'

    return """
    <div class="track-card">
      <div class="player-title">{title}</div>
      {player}
      {meta}
      {lyrics}
    </div>
    """.format(title=title, player=player_html, meta=meta_html, lyrics=lyrics_html)


