def render_menu(nodes, current_slug, is_submenu=False):
    cls = "submenu" if is_submenu else "menu"
    html = ['<ul class="{}">'.format(cls)]
    if not is_submenu:
        home_cur = ' aria-current="page"' if current_slug == "" else ''
        html.append('<li><a href="{href}"{cur}>Головна</a></li>'.format(
            href=href_from(current_slug, ""), cur=home_cur))
    for slug, title, children, _, _ in nodes:
        href = href_from(current_slug, slug)
        is_current = (slug == current_slug)
        cur_attr = ' aria-current="page"' if is_current else ''
        if children:
            descendant_slugs = _flatten_slugs(children)
            open_attr = ' open' if (is_current or current_slug in descendant_slugs) else ''
            html.append('<li><details{open}>'.format(open=open_attr))
            html.append('<summary><a href="{href}"{cur}>{title}</a></summary>'.format(href=href, cur=cur_attr, title=title))
            html.append(render_menu(children, current_slug, is_submenu=True))
            html.append('</details></li>')
        else:
            html.append('<li><a href="{href}"{cur}>{title}</a></li>'.format(href=href, cur=cur_attr, title=title))
    html.append('</ul>')
    return "\n".join(html)


def _flatten_slugs(nodes):
    out = []
    for slug, title, children, _, _ in nodes:
        out.append(slug)
        if children:
            out.extend(_flatten_slugs(children))
    return out


def render_breadcrumb(current_slug, title):
    chain = PARENT_CHAIN[current_slug] + [(current_slug, title)]
    parts = ['<a href="{}">Головна</a>'.format(href_from(current_slug, ""))]
    for i, (slug, t) in enumerate(chain):
        if i == len(chain) - 1:
            parts.append('<span class="sep">/</span><span class="current">{}</span>'.format(t))
        else:
            parts.append('<span class="sep">/</span><a href="{}">{}</a>'.format(href_from(current_slug, slug), t))
    return '<nav class="breadcrumb" aria-label="Хлібні крихти">' + "".join(parts) + "</nav>"


